---- Ruby Programming Language 

--Introduction
Ruby is a dynamic, open-source, object-oriented programming language known for its simplicity and productivity.  
It was created by Yukihiro “Matz” Matsumoto* in the mid-1990s and is popular for web development, especially through the Ruby on Rails framework.

--Key features of Ruby:
- Easy-to-read syntax (close to natural language)
- Pure object-oriented (everything is an object)
- Flexible and expressive
- Large library of gems (packages) for various tasks

---

--How to Install Ruby

--Windows
1. Visit [https://rubyinstaller.org/downloads/](https://rubyinstaller.org/downloads/).
2. Download Ruby+Devkit (x64) version.
3. Run the installer and check “Add Ruby executables to your PATH”.
4. When prompted, run the MSYS2 installation for development tools.

--Mac
1. Open Terminal.
2. Install Homebrew (if not installed):
   ```bash
   /bin/bash -c "$(curl -fsSL https://raw.githubusercontent.com/Homebrew/install/HEAD/install.sh)"

--Verify Ruby installation:
  ruby -v (in cmd or terminal)

