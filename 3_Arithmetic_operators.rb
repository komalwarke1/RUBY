#Arithmetic operators in ruby are used to perform mathematical operations on numbers.
# The basic arithmetic operators in Ruby are:
# + (addition), - (subtraction), * (multiplication), / (division), % (modulus), ** (exponentiation)
# Example of arithmetic operations in Ruby
a = 10
b = 3

puts a + b   # Addition => 13
puts a - b   # Subtraction => 7
puts a * b   # Multiplication => 30
puts a / b   # Division (integer division) => 3
puts a % b   # Modulus => 1
puts a ** b  # Exponentiation => 1000
puts "Arithmetic operations completed successfully!"